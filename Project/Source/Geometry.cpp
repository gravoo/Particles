void Geometry::generate_and_bind_buffered_objects()
{
    glGenVertexArrays(VAO.size(), VAO.data());
    glGenBuffers(VBO.size(), VBO.data());
    glGenBuffers(EBO.size(), EBO.data());

    //cube
    glBindVertexArray(VAO[0]);
    glBindBuffer(GL_ARRAY_BUFFER, VBO[0]);
    glBufferData(GL_ARRAY_BUFFER, sizeof(float) * cube.size(), cube.data(), GL_STATIC_DRAW);
    // position attribute
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    // texture coord attribute
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(1);

    glBindBuffer(GL_ARRAY_BUFFER, 0);
    glBindVertexArray(0);
}

unsigned int Geometry::get_Vertex_Array_Object(unsigned int index)
{
    return VAO[index];
}
Geometry::~Geometry()
{
    glDeleteVertexArrays(VAO.size(), VAO.data());
    glDeleteBuffers(VBO.size(), VBO.data());
    glDeleteBuffers(EBO.size(), EBO.data());
}
